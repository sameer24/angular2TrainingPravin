//Mathematical Operations

export class Calculator {
    constructor() {
        this.no1;
        this.no2;
    }
    
    addNumbers(no1, no2) {
        return no1 + no2;
    }

    subtractNumber(no1, no2) {
        return no1 - no2;
    }

    multiplication(no1, no2) {
        return no1 * no2;
    }

    division(no1, no2) {
        return no1 / no2;
    }
}